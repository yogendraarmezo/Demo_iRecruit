package com.msil.irecruit.dms.payload;

public class OutletPayload {
	private String outlet_name;
	private String outlet_code;
	private String city_name;
	private String state_name;
	private String region_code;
	private String parent_dealer_code;
	private String parent_dealer_name;
	private String location;
	public String getOutlet_name() {
		return outlet_name;
	}
	public void setOutlet_name(String outlet_name) {
		this.outlet_name = outlet_name;
	}
	public String getOutlet_code() {
		return outlet_code;
	}
	public void setOutlet_code(String outlet_code) {
		this.outlet_code = outlet_code;
	}
	public String getCity_name() {
		return city_name;
	}
	public void setCity_name(String city_name) {
		this.city_name = city_name;
	}
	public String getState_name() {
		return state_name;
	}
	public void setState_name(String state_name) {
		this.state_name = state_name;
	}
	public String getRegion_code() {
		return region_code;
	}
	public void setRegion_code(String region_code) {
		this.region_code = region_code;
	}
	public String getParent_dealer_code() {
		return parent_dealer_code;
	}
	public void setParent_dealer_code(String parent_dealer_code) {
		this.parent_dealer_code = parent_dealer_code;
	}
	public String getParent_dealer_name() {
		return parent_dealer_name;
	}
	public void setParent_dealer_name(String parent_dealer_name) {
		this.parent_dealer_name = parent_dealer_name;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	
	
	
}
